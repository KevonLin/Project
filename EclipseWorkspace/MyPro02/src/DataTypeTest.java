public class DataTypeTest {

	public static void main(String[] args) {
		char a = 't';
		char b = 'b';
		
		System.out.println('a' + 'b');
		
		System.out.println("" + 'a' + 'b');
		
		//转义字符
		/* \b 退格
		 * \n 换行
		 * \r 回车
		 * \t 制表符
		 * 
		 */
	}
}
