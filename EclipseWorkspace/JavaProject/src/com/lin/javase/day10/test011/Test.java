package com.lin.javase.day10.test011;

/**
 * 实例语句块/代码块【了解内容，使用的非常少】
 * 	1、实例代码块可以编写多个，遵循自上而下的顺序执行
 * 	2、实例代码块在构造方法执行前执行，构造方法执行一次，实例代码块执行一次
 * 	3、实例代码块也是Java语言为程序员准备的一个特殊时机，被称为对象初始化时机。
 * @author Lin
 *
 */
public class Test {
	
	//构造函数
	public Test(){
		System.out.println("Test类的缺省构造器执行");
	}
	
	//实例代码块
	{
		System.out.println(1);
	}
	//实例代码块	
	{
		System.out.println(2);
	}
	//实例代码块	
	{
		System.out.println(3);
	}
	
	//主方法
	public static void main(String[] args) {

		System.out.println("main begin");
		new Test();
		new Test();
	}

}
